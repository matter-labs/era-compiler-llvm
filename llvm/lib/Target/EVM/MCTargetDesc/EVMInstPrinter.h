//=------ EVMInstPrinter.h - Convert EVM MCInst to assembly syntax -*- C++ -*-//
//
// This class prints a EVM MCInst to a .s file.
//
//===----------------------------------------------------------------------===//

#ifndef LLVM_LIB_TARGET_EVM_MCTARGETDESC_EVMINSTPRINTER_H
#define LLVM_LIB_TARGET_EVM_MCTARGETDESC_EVMINSTPRINTER_H

#include "llvm/MC/MCInstPrinter.h"

namespace llvm {
class EVMInstPrinter final : public MCInstPrinter {
public:
  EVMInstPrinter(const MCAsmInfo &MAI, const MCInstrInfo &MII,
                 const MCRegisterInfo &MRI)
      : MCInstPrinter(MAI, MII, MRI) {}

  void printRegName(raw_ostream &OS, unsigned RegNo) const override;
  void printInst(const MCInst *MI, uint64_t Address, StringRef Annot,
                 const MCSubtargetInfo &STI, raw_ostream &O) override;

  // Used by tblegen code.
  void printOperand(const MCInst *MI, unsigned OpNo, raw_ostream &O);

  // Autogenerated by tblgen.
  std::pair<const char *, uint64_t> getMnemonic(const MCInst *MI) override;
  void printInstruction(const MCInst *MI, uint64_t Address, raw_ostream &O);
  static const char *getRegisterName(unsigned RegNo);
};
} // namespace llvm

#endif // LLVM_LIB_TARGET_EVM_MCTARGETDESC_EVMINSTPRINTER_H
